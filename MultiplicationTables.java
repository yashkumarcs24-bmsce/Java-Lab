class MultiplicationTables{
    public static void main(String[] args) {
        System.out.println("Multiplication table of 3 and 5");
        for (int i = 1; i <= 10; i++) {
            System.out.println("3x" + i + "=" + 3 * i);
        }
        for (int i = 1; i <= 10; i++) {
            System.out.println("5x" + i + "=" + 5 * i);
        }
    }
}